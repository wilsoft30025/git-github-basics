/* 
    DELETING GIT COMPLETELY FROM THE WORKING DIRECTORY
    ---
        Let's say you started a project with the "git init" and you realized that you want to completely DELETE the git from your working directory AND start again...

        In you working directory execute the following command
            -----------
            rm -rf .git
            -----------
                This will permanently delete the ".git" folder. So therefore you can re-initialize your Git Again using "git init"
*/
