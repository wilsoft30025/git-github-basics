/* 
    UNDERSTANDING GITHUB ACCOUNT TYPES
    ---
        GitHub has 3 different Account Types

            1. PERSONAL User Account (Free Pricing Plan)
            -----
                -> every GitHub user's user account
                -> unlimited Public & Private repositories
                -> unlimited number of collaborators (github users/accounts)


            2. ORGANIZATIONAL Account
            -----
                -> shared account for groups of people to collaborate
                -> has base features as personal account
                -> add advanced features with GitHub Team/Enterprise Cloud Plan
                

            3. ENTERPRISE Account
            -----
                -> central management of multiple github account
                -> github enterprise cloud and github server
                -> enterprise level only
*/
